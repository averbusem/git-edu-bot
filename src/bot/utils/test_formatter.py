def format_question_text(question_data: dict) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Ç–µ–∫—Å—Ç –≤–æ–ø—Ä–æ—Å–∞ —Å –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏ –æ—Ç–≤–µ—Ç–∞.
    –ü—Ä–∏–º–µ—Ä —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
    üìå 1. –ß—Ç–æ —Ç–∞–∫–æ–µ Git?

    A) –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —Ñ–∏–Ω–∞–Ω—Å–æ–≤—ã—Ö —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–π
    B) –†–∞—Å–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –≤–µ—Ä—Å–∏—è–º–∏
    C) –õ–æ–∫–∞–ª—å–Ω—ã–π —Ñ–∞–π–ª–æ–≤—ã–π –º–µ–Ω–µ–¥–∂–µ—Ä
    D) –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∏ —à–∏—Ñ—Ä–æ–≤–∞–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö
    """
    options_text = "\n".join(f"{letter}) {text}" for letter,
                             text in question_data["options"].items())
    return f"{question_data['question']}\n\n{options_text}"


def format_question_summary(question_data: dict, user_answer: str) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∏—Ç–æ–≥–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –æ—Ç–≤–µ—Ç–∞ –∏ –æ—Ç–º–µ—á–µ–Ω–Ω—ã–º–∏ –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏.
    –ü—Ä–∏–º–µ—Ä —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –ø—Ä–∏ –≤–µ—Ä–Ω–æ–º –æ—Ç–≤–µ—Ç–µ:
    –í–µ—Ä–Ω–æ!
    üìå 1. –ß—Ç–æ —Ç–∞–∫–æ–µ Git?
    A) –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —Ñ–∏–Ω–∞–Ω—Å–æ–≤—ã—Ö —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–π
    B) –†–∞—Å–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –≤–µ—Ä—Å–∏—è–º–∏ ‚úÖ
    C) –õ–æ–∫–∞–ª—å–Ω—ã–π —Ñ–∞–π–ª–æ–≤—ã–π –º–µ–Ω–µ–¥–∂–µ—Ä
    D) –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∏ —à–∏—Ñ—Ä–æ–≤–∞–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö
    """
    result_msg = "<b>–í–µ—Ä–Ω–æ</b>" if user_answer == question_data["correct"] else "<b>–ù–µ–≤–µ—Ä–Ω–æ</b>"
    marked_options = []
    for letter, text in question_data["options"].items():
        mark = "‚úÖ" if letter == question_data["correct"] else ""
        marked_options.append(f"{letter}) {text} {mark}")
    return f"{result_msg}\n\n{question_data['question']}\n" + "\n".join(marked_options)
